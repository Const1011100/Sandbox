/*
Дано математичне рівняння, яке містить *,+,-,/, у зворотному порядку:

solve("100*b/y") = "y/b*100"
solve("a+b-c/d*30") = "30*d/c-b+a"
*/

// Мій варіант
function solve(equation) {
  return equation
    .split(/([+\-*/])/)
    .reverse()
    .join('');
}
// Цікаві варіанти з Codewars
function solve_(eq) {
  return eq
    .split('/')
    .join(' / ')
    .split('+')
    .join(' + ')
    .split('*')
    .join(' * ')
    .split('-')
    .join(' - ')
    .split(' ')
    .reverse()
    .join('');
}
