/*
Напишіть функцію, яка отримує два рядки як параметр. Ці рядки мають такий формат дати: РРРР/ММ/ДД.
Ваше завдання: взяти роки та обчислити різницю між ними.

Приклади:
'1997/10/10' та '2015/10/10' -> 2015 - 1997 = повертає 18
'2015/10/10' та '1997/10/10' -> 2015 - 1997 = повертає 18
На цьому рівні вам не потрібно перевіряти місяці та дні для обчислення різниці.
*/

// Мій варіант
var howManyYears = function (date1, date2) {
  const number1 = date1.split('/');
  const number2 = date2.split('/');
  const result = +number1[0] - +number2[0];

  return result < 0 ? result * -1 : result;
};
