function tailSwap(arr) {
  const [first, second] = arr;
  const [firstHead, firstTail] = first.split(':');
  const [secondHead, secondTail] = second.split(':');

  return [`${firstHead}:${secondTail}`, `${secondHead}:${firstTail}`];
}
// Цікаві варіанти з Codewars
function tailSwap_(arr) {
  let newArr = arr.map((string) => string.split(':'));
  return [newArr[0][0] + ':' + newArr[1][1], newArr[1][0] + ':' + newArr[0][1]];
}

