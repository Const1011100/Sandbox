// Мій варіант
function generateRange(min, max, step){
    let result = [];
    for (let i = min; i <= max; i += step) {
        result.push(i);
    }
    return result;
}

// Цікаві варіанти з Codewars\
function generateRange_(min, max, step) {
    return min > max ? [] : [min, ...generateRange(min + step, max, step)];
}

